#include "raylib.h"
#include "pause.h"

void game(int *select)
{
    // Initialization
    //--------------------------------------------------------------------------------------
    int framesCounter = 0;
    bool exitWindow = false;
    bool pause = false;
    //--------------------------------------------------------------------------------------

    // Main game loop
    while (!exitWindow && !WindowShouldClose()) 
    {

        // Update
        //----------------------------------------------------------------------------------

        if (IsKeyPressed(KEY_ESCAPE))
        {
            pause = true;
        }

        if (!pause)
        {
            
        }
        else
        {
            framesCounter++;
        }
        //----------------------------------------------------------------------------------

        // Draw
        //----------------------------------------------------------------------------------
        BeginDrawing();

        ClearBackground(BLACK);

        DrawText("Hello World!", 100, 100, 20, WHITE);

        EndDrawing();
        //----------------------------------------------------------------------------------

        if (WindowShouldClose())
        {
            *select = 4;
            break;
        }
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------
    
    //--------------------------------------------------------------------------------------
}